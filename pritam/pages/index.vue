import AOS from "aos";
import "aos/dist/aos.css";

export default {
  name: "Home",

  data() {
    return {
      formData: {
        name: '',
        email: '',
        tel: '',
        location: '',
        message: ''
      }
    };
  },

  mounted() {
    AOS.refresh();
    this.openModal();
    // this.$refs.modalTrigger.click();
  },

  methods: {
    openModal() {
      const token = document.cookie.split('; ').find(row => row.startsWith('quantsoftEnquiry='));
      
      if (!token) {
        this.$refs.modalTrigger.click();
        document.cookie = `quantsoftEnquiry=true; max-age=${60 * 60}; path=/`; // Set cookie with 1-hour expiration
      }
    },


    async submitForm() {
      try {
        const response = await fetch('https://quantsoftindia.com/home-enquiry.php', {
          method: 'POST',
          headers: {
            'Content-Type': 'application/x-www-form-urlencoded',
          },
          body: new URLSearchParams(this.formData).toString()
        });

        if (response.ok) {
        
          alert("Message sent successfully!");
        } else {
          
          alert("There was an error sending your message. Please try again.");
        }
      } catch (error) {
        console.error("Error:", error);
        alert("There was an error sending your message. Please try again.");
      }
    }
  
  },

  
};
